private void Adicionar() {
        BtnCalabresa.addActionListener(evt -> SalvarPizzas("Calabresa",19.00));
        BtnMussarela.addActionListener(evt -> SalvarPizzas("Mussarela",18.00));
        BtnFrangoCatu.addActionListener(evt -> SalvarPizzas("Frango e Catupiry",22.00));
        BtnChocolate.addActionListener(evt -> SalvarPizzas("Chocolate",20.50));

        BtnFinalizar.addActionListener(evt -> MostrarPizzas());
    }
    private void SalvarPizzas(String pizzaSabor, double preco) {
        selectedPizzas.add(new Pizza(pizzaSabor, preco));
           Precototal += preco;
        JOptionPane.showMessageDialog(this, "Pizza Escolhida: " + pizzaSabor);
    }
  private void MostrarPizzas() {
        // Process the selected pizzas in the ArrayList
        StringBuilder message = new StringBuilder("Pizzas Escolhidas:\n");
        for (Pizza pizza : selectedPizzas) {
            message.append(pizza.getSabor()).append(" - R$").append(pizza.getPreco()).append("\n");
        }
         message.append("Pre√ßo Total: R$").append(Precototal);
        JOptionPane.showMessageDialog(this, message.toString());
    }

